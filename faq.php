            <p class="question" id="q8"><span>Q</span>Should I worry about hackers?</p>
            <p class="answer"><span>A</span>Don't lose any sleep worrying about being hacked. If you're just a typical
                home computer user, i.e. you're not <abbr>NASA</abbr> or the World Bank, why is someone going to hack
                you? If I or another competent technician has set up your computer or network, there should be plenty of
                protection in place for your computers. If you are using a router, that is one form of firewall, and the
                software firewall that comes with Windows is another layer of protection. If your accounts are
                passworded as well, and you are not using the default password that came with your router, then a hacker
                has got his work cut out for him. Remotely hacking into a computer is time consuming. Imagine someone
                using all his skill and knowledge to access your home network from afar, just so that he can get into
                your computer and then... then what? Read your email? Look at your pictures? Can you really imagine that
                anyone will go to the trouble? My point is that unless you have sensitive data stored on your computer,
                it's not likely that you'll ever be hacked by anyone who knows what they're doing.</p>
            <p>Software companies want you to be paranoid about things like that so that you'll buy their "protection"
                products. Don't worry about it too much. For your home computer, a properly configured router plus the
                software firewall included in XP <abbr class="tooltip"
                                                       title="Service Pack 2 - This was a large update to Windows XP that you should absolutely have installed when it came out. You probably have it, but if you're not sure, right-click My Computer and click Properties. If you see Service Pack 2 there, you have it.">SP2</abbr>
                and Vista is plenty. If you want to be sure that you're reasonably secure, <a href="contact.php">give me
                    a call</a> and I'll be happy to check it out.</p>
