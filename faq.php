            <p class="question" id="q9"><span>Q</span>What kind of home computer should I buy?</p>
            <p class="answer"><span>A</span>For just a budget, limited-use home computer, you can wait for Dell to run a
                special and call me to set it up after it arrives. I'll want to uninstall all the superfluous programs
                that they preinstall at the factory, and install free antivirus and antispyware programs. I think that
                Dell still builds pretty good computers.</p>
            <p>New Compaq or HP computers seem to run pretty well too. Truth is, there's not one brand of computer out
                there that is the greatest and always runs without problems. I hate to recommend one or the other,
                because if they send you a lemon, you might blame me! You might wish to have your computer built locally
                by a shop. Expect it to be a little more expensive, but the local service should be better. Just don't
                ever buy a computer from WalMart. The computers I've worked on that came from that store have been the
                worst I've seen. In particular, the power supplies and motherboards that manufacturers use for these
                discount machines seem to die like light bulbs.</p>
            <p>If you're looking to buy, check out <a href="http://www.pcworld.com/reviews/hardware/"
                                                      onclick="window.open(this.href,'_blank');return false;">pcworld.com's
                    hardware reviews</a> to see what's currently getting high marks. They're probably as reliable a
                source as there is for information like that.</p>
            <p>Whatever you order, remember to demand an <abbr class="tooltip" title="Original Equipment Manufacturer">OEM</abbr>
                copy of your operating system! You're paying for it; part of the cost of that computer is the copy of
                Windows that they install on it. Make sure they include this disk, because they probably won't unless
                you request it specifically. You might need it one day. <em>Don't accept "restore" disks in place of the
                    actual operating system.</em> If they refuse to supply a copy of the operating system that you're
                paying for, do business elsewhere.</p>
