        <div class="col_3">
            <p class="question" id="q6"><span>Q</span>What's the difference between memory and storage?</p>
            <p class="answer"><span>A</span>Imagine going to work and pulling from a filing cabinet all the files that
                you will need to work with that day. You spread all your files out onto the top of your desk. From your
                desktop, you read them, make changes to them, rename them, etc. You may create some new files on your
                desk. You retrieve other files as you need them from your filing cabinet and lay them onto your desk.
                When you're done working on your files, you place all of them back into your filing cabinet, leaving the
                surface of your desk clean.</p>
            <p>Your filing cabinet is <em>storage</em>. The top of your desk is <em>memory</em>. A computer uses hard
                disk drives for storage and <abbr class="tooltip" title="Random Access Memory">RAM</abbr> for memory.
                When you start your computer, it copies the files it needs in order to run your programs from the hard
                disk to memory, where it can work with them much, much faster. When you shut the computer down,
                everything is moved from memory back to the hard drive for keeping.</p>
        </div>
        <div class="col_3">
            <p class="question" id="q7"><span>Q</span>Will adding more <abbr>RAM</abbr> make my computer faster?</p>
            <p class="answer"><span>A</span>Probably. If your desk had more surface area, you could place more files
                onto it, and work with them more easily because they wouldn't be all mashed together. It's about having
                more space to work with.</p>
            <p>In the Windows XP days, computer companies sold a lot of new computers with insufficient memory, but
                that's not a big problem any more. If your computer seems very slow, memory is an obvious thing to look
                at for speeding it up. I recommend running XP on <em>at least</em> 512 MB of <abbr>RAM</abbr>, Vista on
                at least 3GB and Win7 on at least 2GB. That's <em>at least</em>. If you're looking for a new computer,
                4GB is a good number to look for. Don't buy a new computer that has Vista or 7 with less than 4GB of RAM
                installed, and you should have plenty of RAM for most applications.</p>
            <p>Compulsively adding memory to a computer is not a cure-all, though. Don't expect to change a ten year old
                computer into a speed demon by adding memory.</p>
