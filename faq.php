            <p class="question" id="q1"><span>Q</span>Should I upgrade my computer or just buy a new one?</p>
            <p class="answer"><span>A</span>This is too general a question to answer with a simple yes or no. It depends
                on several things - not least, what specifically you want to upgrade. The first thing I would ask is the
                age of the computer. I might want to know what it cost you. If it's a ten year old clunker, just replace
                the thing and donate your old one to the local library. Computers used to be expensive, but that isn't
                the case any more. It's not uncommon nowadays to find brand new computers - perfectly capable machines -
                on sale for $300. Paying someone to upgrade your hard drive and memory can possibly approach that
                figure. What makes more sense to you?</p>
        </div>
        <div class="col_3">
            <p class="question" id="q2"><span>Q</span>Should I switch to Linux?</p>
            <p class="answer"><span>A</span>Probably not. If you're used to Windows, and you consider yourself "computer
                illiterate", then Linux is not for you. There will be a steep learning curve as you have to learn new
                ways of installing software, for instance. Also, if you've become dependent on commercial software like
                Microsoft Office, TurboTax, and other programs that you can buy in the store (including games), and you
                are not interested in learning new ways of getting your work done, be aware that most commercial
                software will not run on Linux. On the other hand, if all you ever do is email and look at websites,
                it's possible that you could start using Linux tomorrow and never miss Microsoft's products.</p>
        </div>
        <div class="col_3">
            <p class="question" id="q3"><span>Q</span>Are peer-to-peer file sharing programs like Limewire and Bearshare
                safe to use?</p>
            <p class="answer"><span>A</span>Not really. For one, when someone uses such a program to download and share
                copyrighted music and video, they are breaking the law. Also, these programs usually come bundled with
                spyware and adware. And just because a file is named "popularsong.mp3" doesn't mean that it is actually
                that song. Anyone can name a virus-infected file "popularsong.mp3" and offer it for download. In other
                words, it is very, very common for people to unwittingly download viruses and malware to their computers
                when they think that they're only downloading music. I see this <em>all the time!</em></p>
            <p>If you don't understand why it is a bad idea to download an mp3 that is named after a popular song, but
                is only 25 kb in size, you should not use a file sharing program. If you don't understand what that last
                sentence means, you <em>definitely</em> should not use a file sharing program. You will almost certainly
                download malware to your computer.</p>
