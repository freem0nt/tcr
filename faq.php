            <p class="question" id="q10"><span>Q</span>Why don't you have any of those certifications that IT people
                get?</p>
            <p class="answer"><span>A</span>Here I'll quote a passage from <cite>Start Your Own Computer Business</cite>,
                an invaluable book by <a href="http://www.fonerbooks.com/compbiz.htm"
                                         onclick="window.open(this.href,'_blank');return false;">Morris Rosenthal</a>.
            </p>
            <blockquote><p>How do you establish credentials and why should anybody trust you? Well, it turns out that
                    the best way to convince somebody that you're a computer expert is to show up and do something for
                    them. Outside of the human resources departments of bland companies, nobody knows or cares what an
                    A+ certified technician or a certified Microsoft or Novell engineer is. When you're in business for
                    yourself, people will buy PCs from you because you show up and sell them. Nobody will ask you where
                    you learned to put them together (or to buy them cheap), anymore than you ask the person who comes
                    and installs the phone lines where they learned to do it. It's enough that they show up. In small
                    business, your credentials are your honest face, your word of mouth, your Yellow Pages listing, your
                    website, and your storefront if you have one. You're going to need all the cash you can get your
                    hands on. Don't waste it on pieces of paper that nobody will read to frame and stick on the wall.
                    That's a game for the people who want to be somebody else's employee.&rdquo;</p></blockquote>
            <p>I have personally witnessed an <span class="tooltip"
                                                    title="Holders of the A+ certification have been trained in installing, repairing, troubleshooting, maintaining, customizing and operating PCs.">A+ certified</span>
                individual who did not know how to replace a hard drive. I want to believe that the majority of the
                people who have various certifications truly know their stuff, but all certification really proves is
                that they took a difficult multiple-choice exam, and answered correctly a high enough percentage of
                questions in order to pass. As my anecdote demonstrates, there's no substitute for hands-on experience.
            </p>
            <p>The short answer to the question is that I've never taken any of the certification exams. Maybe one day I
                will study up and get the A+ and the <abbr class="tooltip" title="Microsoft Certified Systems Engineer">MCSE</abbr>
                or Linux+. Or maybe not. Certifications are not a priority to me.</p>
