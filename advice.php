            <h3 id="forwards">&#8226; Be sceptical of forwarded email &#8226;</h3>
            <p>Ok, not exactly Windows advice, but good advice nevertheless.</p>
            <p>Emails that are marked "PLEASE READ! URGENT!!" and/or that plead with you to "FORWARD THIS TO EVERYONE
                YOU KNOW!!!" are almost always hoaxes. Always approach them with this attitude. When you receive such an
                email, select the first sentence in it (or any portion that has specific info) with your mouse, and
                search <a href="http://www.google.com/"
                          onclick="window.open(this.href,'_blank');return false;">Google</a> for this portion of the
                text. Odds are great that you will see a list of links that describe the hoax and give background
                information, such as when the hoax was first reported. Most of them have been around for years.</p>
            <p>You will never rescue a missing little boy by forwarding an email to fifty people. Bill Gates is never
                going to mail a check to you for forwarding email. It will not make any difference in anything
                whatsoever if you don't buy gas on a particular day of the year. There is no such thing as a "Zero
                Sector" on computer hard disks.</p>
            <p>Consider the logic behind a common email hoax that claims a computer virus is spreading via an email
                that, once opened, destroys the "Zero Sector", shuts your computer down, and prevents it from ever
                booting again: If the email shuts down every computer it is opened on, and those computers never boot
                again... how is the virus supposed to spread?? Through some sort of hard drive telepathy?</p>
            <p>Most of these hoax emails similarly defy common sense. A common hoax email is the "Olympic Torch" virus
                email, which warns of "a virus that opens an Olympic Torch which burns the whole hard disc C of your
                computer", and declares that this virus is "the worst virus announced by CNN". If you receive such an
                email, ask yourself: Why haven't I heard about it on the news? Why am I only hearing about this horrible
                virus through a chain email?</p>
            <p>Before you fire off a forward to dozens of people, it's a <span
                        style="text-decoration:line-through">good</span> great idea to check out the facts first, and
                once you establish it's a hoax, send the sender of the forward a gentle note explaining that he/she has
                fallen prey to a prank.</p>
