            <h3>&#8226; Keep your computer clean &#8226;</h3>
            <p>No, I'm not referring to the case itself, although it doesn't hurt anything to give the tower a good
                wiping-off with a soft cloth dampened with kitchen cleaner. I'm referring to your operating system.
                Windows is inherently buggy. For all its operations, it depends on a collection of files called the
                "registry".</p>
            <p> Every program you install on your computer makes changes to the registry. Sometimes a program will
                change the registry in a way that affects the behavior of another program. Sometimes that "other
                program" is a part of Windows itself. When Explorer, the "shell" of Windows, gets messed with by some
                other program's installation or operation, the computer can become unstable, or worse, unbootable. This
                happens a <em>lot</em>.</p>
            <p>The lesson here is an important one:</p>
            <p style="font-size:2em;text-align:center;line-height:1em;">If you do not <em>NEED</em> it, do not install
                it !!</p>
            <p>The surest way to ensure difficulty with your computer is to install every program, game, system utility,
                emoticon pack and screensaver you can get your hands on. Bear in mind, the words "Free Download" do not
                mean "You MUST Download Me Now". Also bear in mind the fact that many of these free downloads may come
                bundled with malware.</p>
            <p>Resist the temptation to try every new program you see, leave your system as clean as possible, and you
                will better your odds for worry-free computing.</p>
